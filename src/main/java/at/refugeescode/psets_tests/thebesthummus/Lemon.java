package main.thebesthummus;

public class Lemon {

    private String name;

    private Integer squeezes;

    public Lemon(Integer squeezes) {
        this.name = "Lemon";
        this.squeezes = squeezes;
    }

    public String getName() {
        return name;
    }

    public Integer getSqueezes() {
        return squeezes;
    }
}
